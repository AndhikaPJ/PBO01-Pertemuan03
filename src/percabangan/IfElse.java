package percabangan;

public class IfElse {
    public static void main(String[] args) {
        double totalBelanja = 25000;
        double uangDiDompet = 50000;

        if (uangDiDompet < totalBelanja) {
            System.out.println("Nah duitnya kada mayu, kurangi belanjaanya!");
        } else {
            System.out.println("Alhamdulillah, belabih bawa duit sekalinya. Happy mukbang!");
        }
    }
}
